


public class variaveis {
	
	public static void main(String[] args){
		// Declaração de variáveis
		
		int anoAtual;
		int valor = 10;
		double valor2 = 20.5;		
		boolean teste = true;
		float valor3 = 54.57F; //Sempre que for atribuir um valor a uma variável, adicione uma letra F ao final do valor
		String nome = "Eduardo Inocencio"; //A string é uma classe
		char letra = 'a'; //Para inserir um valor char, o mesmo deve estar entre aspas simples
		char letra2 = 97; // ASCII - Caractere correspondente ao código da tabela
		char letra3 = '\u004B'; //Código unicode (Mapa de caracteres)
		
		//Alimentação das variáveis
		anoAtual = 10;
		
		
		//Exibindo a variável
		System.out.println(valor);
		System.out.println(valor2);
		System.out.println(teste);
		System.out.println(valor3);
		System.out.println(nome);
		System.out.println(anoAtual);
		System.out.println(letra);
		System.out.println(letra2);
		System.out.println(letra3);
	}
	

}
